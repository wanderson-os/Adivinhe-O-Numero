    private void JBverificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBverificarActionPerformed

        vazio = "".equals(TFdigiteUmNumero.getText());
        if (vazio == false) {
            tentativa += 1;
            if (tentativa >= 5) {
                TFtentativa.setText(String.valueOf(tentativa));
                resposta = Integer.parseInt(TFdigiteUmNumero.getText());
                if (resposta == aleatorio) {
                    JOptionPane.showMessageDialog(null, "O número era mesmo o " + String.valueOf(resposta) + "\nAcertou na ultima tentativa!");
                    acerto();
                } else {
                    JOptionPane.showMessageDialog(null, "Perdeu ! O número era " + String.valueOf(aleatorio) + ".");
                    iniciar();
                }
            } else {
                TFtentativa.setText(String.valueOf(tentativa));
                resposta = Integer.parseInt(TFdigiteUmNumero.getText());
                if (resposta == aleatorio) {
                    JOptionPane.showMessageDialog(null, "Acertou !!!");
                    acerto();
                } else {
                    if (aleatorio > resposta) {
                        TFpainel.setText("Tente com um número maior");
                    } else {
                        TFpainel.setText("Tente com um número menor");
                    }
                    erro += 1;
                    TFerro.setText(String.valueOf(erro));
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite um número!");
        }
    }//GEN-LAST:event_JBverificarActionPerformed

