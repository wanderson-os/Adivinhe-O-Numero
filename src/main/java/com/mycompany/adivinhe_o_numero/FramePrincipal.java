    public void iniciar() {

        aleatorio = pro.geraNumero();
        vazio = true;
        TFdigiteUmNumero.setText("");
        resposta = 0;
        acerto = 0;
        erro = 0;
        tentativa = 0;
        rodada = 0;
        TFacerto.setText("0");
        TFerro.setText("0");
        TFrodada.setText("0");
        TFtentativa.setText("0");
    }

    public void acerto() {
        acerto += 1;
        rodada += 1;
        tentativa = 0;
        TFacerto.setText(String.valueOf(acerto));
        TFrodada.setText(String.valueOf(rodada));
        TFtentativa.setText("0");
        aleatorio = pro.geraNumero();
        resposta = -1;
        TFdigiteUmNumero.setText("");
    }
